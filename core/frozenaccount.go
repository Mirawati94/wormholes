func getBigWei(num string) *big.Int {
	numB, _ := new(big.Int).SetString(num, 10)
	numC, _ := new(big.Int).SetString("1000000000000000000", 10)
	return numB.Mul(numB, numC)
}

func getUnixTimestamp(t string) uint64 {
	tutc, _ := time.Parse("2006-01-02 15:04:05", t)
	timestamp := tutc.Unix()
	return uint64(timestamp)
}

func GetInitFrozenAccounts(data string) *types.FrozenAccountList {
	var frozenAccountList types.FrozenAccountList

	frozenAccountInfos := strings.Split(data, ",")
	for _, frozenAccountInfo := range frozenAccountInfos {
		accountInfo := strings.Split(frozenAccountInfo, "|")
		var frozenAccount types.FrozenAccount
		frozenAccount.Account = common.HexToAddress(accountInfo[0])
		frozenAccount.Amount = getBigWei(accountInfo[1])
		frozenAccount.UnfrozenTime = getUnixTimestamp(accountInfo[2])

		frozenAccountList.FrozenAccounts = append(frozenAccountList.FrozenAccounts, &frozenAccount)
	}

	return &frozenAccountList
}
